	ORG 0000H
	LJMP INICIO	
	ORG 0100H

INICIO:
	MOV TMOD, #01H
	MOV TH0, #0
	MOV TL0, #0
    SETB TR0
	MOV P1, P2
	JB P2.0, INICIO	
	ACALL COMECANDO

COMECANDO:
	MOV R2, #5
	MOV R3, #5
	MOV R6, #5
	MOV R0, #20H
	MOV R1, #30H
	JNB P2.0, COMECANDO
	ACALL SEQ_LED
	RET
	
SEQ_LED:
	MOV A, TH0
    XRL A, TL0     
    ANL A, #00000111B
	MOV @R0, A
	INC R0
	MOV B, A
	MOV A, #00000001B
RANDOMIZAR:
	RL A
    DJNZ B, RANDOMIZAR
    CPL A
    MOV P1, A
    MOV R4, #4
    ACALL DELAY
    MOV P1, #11111111B
    MOV R4, #4
    ACALL DELAY
    DJNZ R3, SEQ_LED
    JMP SEQ_JOGADOR
	
SEQ_JOGADOR:
	JNB P2.0, LED_BOTAO_0
    JNB P2.1, LED_BOTAO_1
	JNB P2.2, LED_BOTAO_2
	JNB P2.3, LED_BOTAO_3
	JNB P2.4, LED_BOTAO_4
	JNB P2.5, LED_BOTAO_5
	JNB P2.6, LED_BOTAO_6
	JNB P2.7, LED_BOTAO_7  
	MOV A, R2
	JNZ SEQ_JOGADOR
	JMP PRE_VERIFICAR

DELAY:
	
NEXT:
	MOV R5, #255
AGAIN:
	DJNZ R5, AGAIN
	DJNZ R4, NEXT
	RET

