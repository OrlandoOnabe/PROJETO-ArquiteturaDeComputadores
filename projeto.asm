PRE_VERIFICAR:
	MOV R0, #20H
	MOV R1, #30H
	JMP VERIFICAR

VERIFICAR:
	MOV A, @R0
	MOV B, @R1
	CJNE A, B, ERROU
	INC R0
	INC R1
	DJNZ R6, VERIFICAR
	JMP ACERTOU

ACERTOU:
	MOV P1, #11111110B
	JMP ACERTOU 

ERROU:
	MOV P1, #11111101B
	JMP ERROU 
