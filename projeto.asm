	ORG 0000H
	LJMP INICIO	
	ORG 0100H

INICIO:
	MOV P1, P2
	MOV R2, #5
	JB P2.0, INICIO	
	ACALL COMECANDO
	
	
SEQ_LED:
	MOV P1, #10111111B
	MOV R4, #4
	ACALL DELAY
	MOV P1, #01111111B
	MOV R4, #4
	ACALL DELAY
	DJNZ R1, SEQ_LED
	MOV P1, #11111111B
	JMP SEQ_JOGADOR
	

SEQ_JOGADOR:
	JNB P2.0, LED_BOTAO_0
    JNB P2.1, LED_BOTAO_1
	JNB P2.2, LED_BOTAO_2
	JNB P2.3, LED_BOTAO_3
	JNB P2.4, LED_BOTAO_4
	JNB P2.5, LED_BOTAO_5
	JNB P2.6, LED_BOTAO_6
	JNB P2.7, LED_BOTAO_7  
	MOV A, R2
	JNZ SEQ_JOGADOR
	JMP INICIO

DELAY:
	
NEXT:
	MOV R5, #255
AGAIN:
	DJNZ R5, AGAIN
	DJNZ R4, NEXT
	RET

COMECANDO:
	MOV R1, #5
	JNB P2.0, COMECANDO
	ACALL SEQ_LED
	RET

LED_BOTAO_0:
	MOV P1, P2
	JNB P2.0, LED_BOTAO_0
	DEC R2
	ACALL SEQ_JOGADOR

LED_BOTAO_1:
	MOV P1, P2
	JNB P2.1, LED_BOTAO_1
	DEC R2
	ACALL SEQ_JOGADOR

LED_BOTAO_2:
