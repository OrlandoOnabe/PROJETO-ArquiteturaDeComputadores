	ORG 0000H
	LJMP INICIO	
	ORG 0100H

INICIO:
	JB P2.0, INICIO
	ACALL PRESSIONADO
	
	
SEQ_LED:
	MOV P1, #00111111B
	MOV R4, #4
	ACALL DELAY
	DEC R1
	MOV P1, #01111111B
	JMP INICIO
	

SEQ_JOGADOR:
	MOV P1, P2

DELAY:
	
NEXT:
	MOV R5, #255
AGAIN:
	DJNZ R5, AGAIN
	DJNZ R4, NEXT
	RET

PRESSIONADO:
	MOV R1, #10
	JNB P2.0, PRESSIONADO
	ACALL SEQ_LED
	RET